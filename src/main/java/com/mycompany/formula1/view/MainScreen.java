    private void showErrorMessage(String message) {
        JOptionPane.showMessageDialog(null, message, "Erro", JOptionPane.ERROR_MESSAGE);
    }
    
    public void updateTeamsTable() {
        try {
            List<Team> teamList = teamController.getAllTeams(); // ou listarTeams()
            DefaultTableModel model = (DefaultTableModel) teamTable.getModel();
            model.setRowCount(0); // limpa a tabela

            for (Team team : teamList) {
                Object[] rowData = {team.getId(), team.getName(), team.getCountry()};
                model.addRow(rowData);
            }
        } catch (Exception e) {
            showErrorMessage(e.getMessage());
        }
    }

    public void updateDriversTable() {
        try {
        List<Driver> driverList = driverController.getAllDrivers(); // ou listarDrivers()
        DefaultTableModel model = (DefaultTableModel) driverTable.getModel();
        model.setRowCount(0); // limpa a tabela

        for (Driver driver : driverList) {
            Object[] rowData = {driver.getId(), driver.getName(), driver.getTeamId()};
            model.addRow(rowData);
        }
        } catch (Exception e) {
           showErrorMessage(e.getMessage());
         }
}
