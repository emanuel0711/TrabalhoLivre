    private void removeTeamActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeTeamActionPerformed

        int selectedRow = teamTable.getSelectedRow();
			
      if (selectedRow != -1) {
        int teamId = (int) teamTable.getValueAt(selectedRow, 0); //Pego o valor da celula que corresponde a linha tal x coluna tal

        try {
            teamController.removeTeam(teamId);
        } catch (Exception e) {
            showErrorMessage(e.getMessage());
        }
      } else {
		showErrorMessage("Selecione uma Equipe!");
      }
        
        updateTeamsTable();
    }//GEN-LAST:event_removeTeamActionPerformed

